                <h5>Lagerungsschwindel:</h5>
                <p>Meist eine vorübergehende (=paroxyxmale), gutartige (= beninge) Störung des Gravitationssensors im Innenohr. Besonders beim Umdrehen im Bett treten extrem starke Drehschwindelanfälle auf, wobei die Drehung immer in die gleiche Richtung erfolgt, nur für einige Sekunden und nur in Verbindung mit Bewegungen. Es tritt nie Bewußtlosigkeit oder Kopfschmerzen auf. Bei Diagnosestellung in der Arztpraxis, können wir diesen Schwindel fast immer mit einem einfachen Lagerungsmanöver am gleichen Tage noch beenden, selten müssen 1 - 2 Wiederholungen erfolgen.</p>
