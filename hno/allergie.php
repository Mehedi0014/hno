                <h5>HYPOSENSIBILISIERUNG (SIT), DIE DAUERHAFTE BEHANDLUNG DER ALLERGIEN</h5>
                <ul>
                  <li>Die sogenannte Hyposensibilisierung (früher oft auch Desensibilisierung genannt, neuerdings SIT) ist ein kompliziertes Thema, das hier nur ganz stark vereinfacht beschrieben werden kann.</li>
                  <li>Bei der Hyposensibilisierung (SIT= Spezifische Immuntherapie) werden mehrfach kleinste Mengen des auslösenden Allergens z.B. unter die Haut gespritzt – in steigender Dosierung über eine längere Zeit. </li>
                  <li>Der genaue Wirkmechanismus ist noch nicht völlig geklärt und die Erfolgsaussichten sind je nach Allergen ganz unterschiedlich, wurden aber in den letzten Jahren ständig verbessert. Diese Prinzip kann man z.B. auch bei Insektengiftallergien anwenden.</li>
                  <li>Die Hyposensibilisierung muß von Ärztin oder Arzt selbst durchgeführt werden.</li>
                  <li>Die Hyposensibilisierung muß über einen Zeitraum von zu mehreren Jahren möglichst regelmäßig durchgeführt werden.</li>
                  <li>Voraussetzungen: Die auslösenden Allergene müssen genau bekannt sein. Während der Hyposensibilisierung sollte der Patient frei von Fieber und/oder akuten Infektionen sein.</li>
                  <li>Früher mußten sich die Patienten wöchentlich in der Praxis zum Spritzen einfinden, seit einiger Zeit gibt es modernere Präparate, die nur monatlich verabreicht werden müssen, also insgesamt nur noch ca. 12x im Jahr. </li>
