                <h5>WIE ENTSTEHT HEUSCHNUPFEN IM KÖRPER?</h5>
                <p>Der Heuschnupfen gehört, wie das allergische Asthma, zu einer Gruppe von Erkrankungen, die auf gleiche Vorgänge im Körper zurückzuführen sind: Ursache ist die Überempfindlichkeit auf verschiedene Umweltstoffe, wie z.B. Blütenpollen. Sie wirken als Allergene und lösen eine immunologische Reaktion aus. Die Neigung zu einer solchen Reaktion ist wahrscheinlich angeboren, obwohl die Entstehung der Allergie selbst wahrscheinlich im Zusammenhang mit zu hoher oder zu niedriger Allergenexposition steht. Bei entsprechend empfindlichen Personen führt der erste Kontakt mit einem Allergen zur Entwicklung von natürlichen Abwehrstoffen, sogenannten Antikörpern. Ab diesem Zeitpunkt erkennt der Organismus die harmlosen Substanzen als gefährlich. Er ist "sensibilisiert" und bildet im Laufe von mehreren Jahren immer mehr Antikörper. Irgendwann kommt der Zeitpunkt, an dem der Patient zum ersten Mal bemerkt, daß er krank ist. Es kommt zur ersten allergischen Reaktion. Bei Kontakt mit dem Allergen, z.B. den Blütenpollen, reagiert der Körper jetzt, als müsse er Krankheitserreger bekämpfen. </p>
                <h5>WAS PASSIERT GENAU?</h5>
                <p>Bei Kontakt verbinden sich Antikörper und Allergen und veranlassen bestimmte Körperzellen, Botenstoffe auszuschütten, die die allergische Reaktion im Körper weitervermitteln. Ein solcher Botenstoff ist z.B. das Histamin. Histamin ist eine biologisch sehr aktive Substanz, die überall in unserem Körper vorkommt. Die Wirkung des Histamins hat z.B. jeder schon einmal gespürt, der eine Brennessel angefaßt hat. </p>
                <p>Histamin erweitert die kleinen Blutgefäße. Dadurch kommt es zu einer Rötung. Aus den Blutgefäßen tritt Flüssigkeit ins Gewebe aus und es entsteht eine Schwellung. Zusätzlich wird die Tätigkeit der Drüsen angeregt.</p>
