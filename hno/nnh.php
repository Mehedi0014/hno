                        <h4>Wozu dienen die NNH?</h4>
                        <p>Die NNH haben sich nur aus einem einzigen Grund ausgebildet - Gewichtserleichterung des Schädels. In den Jahrmillionen hat sich unsere Gehirn derart entwickelt, daß die schützende Knochenhülle immer größer und schwerer werden musste. Darüber hinaus mussten wir "Allesfresser" einen ausreichend starken Unterbau für unseren Kiefer als Stützgerüst ausbilden. Und schließlich hat sich in den letzten Jahrtausenden auch die Physiognomie unseres Gesichts sich in Richtung ästhetische Proportionen entwickelt, auch dafür ist ein Knochenunterbau erforderlich.</p>
                        <p>Ein menschlicher Kopf ist schwer und kann über 10 kg wiegen. Nur durch die von der Natur entwickelte Hohlbauweise kann ein noch höheres Gewicht vermieden werden. Diese Hohlräume sind die Nasennebenhöhlen. Im Kiefer- und Stirnbereich formen sie wichtige ästhetische Gesichtsproportionen. Darüber hinaus sind sie über sehr (manchmal zu) dünne Kanäle mit der Nase verbunden. Die Nase selbst (eigentlich die Nasenhaupthöhlen) beginnt bekanntlich an den Nasenlöchern und endet über dem Zäpfchen tief hinten im Rachen (an den Choanen). D.h. Schleim der hinten die Nase herunterläuft kann man z. T. durch den Mund an der Rachen-Hinterwand sehen (wenn man schnell genug guckt! ;-).</p>
                        <h4>Warum werden die NNH krank?</h4>
                        <p>In die Nebenhöhlen gehört - Luft - und nichts anderes. Alles andere darin macht die NNH krank, sei es Schleim, Eiter oder andere Flüssigkeiten wie auch <a href="#">Polypen</a> , Pilze; Tumoren selbstverständlich auch, zum Glück aber nicht sehr häufig.</p>
                        <p>Spritz man sich z.B. mit einer Spritze 10 ml Luft unter die Haut, egal wo, dann entsteht dort eine Beule. Nach einiger Zeit verschwindet diese und löst sich buchstäblich auf - und zwar im Blut. Das Blut bindet die Luft und transportiert sie ab - in der Lunge wird diese dann z. T. wieder frei.</p>
                        <p>Das gleiche passiert auch in den Nebenhöhlen, wenn sich die Belüftungskanäle verschließen. Die verbleibende Luft wird resorbiert und abtransportiert. Es resultiert ein schmerzhafter Unterdruck mit den typischen Kopfschmerzen, dem Druckgefühl im Kopf und der Benommenheit.</p>
                        <p>Das Verstopfen dieser Kanäle kann durch viele verschiedene Ursachen ausgelöst werden. </p>
