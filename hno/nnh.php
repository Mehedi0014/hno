                        <h4>Was mache ich gegen eine beginnende NNH-Entzündung?</h4>
                        <p>Welches Problem auch immer die Belüftung der Nasennebenhöhlen stört - es muß gelöst werden, sonst bekommt man keine Ruhe. Meistens erfolgt dies medikamentös, manchmal aber auch operativ, besonders dann, wenn bereits eine chronische NNH-Problematik  oder eine hoffnungslos verlegte Nase vorliegt. Eine neue sehr schonende Operationsmethode stellt die <a href="#">Sinuplastie</a> dar, leider gibt es nur wenig Indikationen dafür.</p>
                        <p>Wie der werte Leser schon bemerkt haben wird, ist die freie Nase eine Grundvoraussetzung für das Genesen der NNH. Also sollten stark lokal abschwellende Nasentropfen - wenn gesundheitlich erlaubt - angewendet werden, nicht länger als max. 1 Woche. Diese Tropfen machen dann nicht nur die Nase, sondern auch - und das ist noch wichtiger - auch die Verbindungkanäle zu den NNH frei. Dazu ein schleimlösendes Mittel, pflanzlich oder chemisch, und reichlich Flüssigkeitszufuhr. Je flüssiger das Sekret in den NNHs wird, desto besser kann es ablaufen und der Luft die verschleimte Höhle überlassen. Falls die Verstopfung durch allergische Schwellungen verursacht wird, müssen ebenfalls noch spezielle anithistaminerge oder <a href="#">kortisonhaltige</a> <a href="#">Medikamente</a> verwendet werden.</p>
                        <p>Beim Auftreten von Fieber oder starken Schmerzen sollten sie auf alle Fälle den Arzt aufsuchen, evtl. muß ein Antibiotikum oder andere starke Medikamente angewendet werden. Auch sollten sie eine ärztliche Untersuchung durchführen lassen, wenn die Probleme längere Zeit vorliegen oder gehäuft auftreten</p>
                        <h4>Was sind eigentlich die operativen Prinzipien?</h4>
                        <p>Ganz klar: Die Nase muß frei werden und/oder  die Eingänge zu den Nebenhöhlen größer. Ein verbogenes <a href="#">Septum</a> sollte begradigt werden, <a href="#">vergrößerte Nasenmuscheln</a> verkleinert, <a href="#">Polypen</a> entfernt. </p>
                        <p>Dazu ist manchmal eine Nebenhöhlenoperation, also Kieferhöhlenoperation, Stirnhöhlenoperation, Keilbeinhöhlenoperation oder Siebbeinoperation erforderlich, eine Operation sämtlicher, bzw. mehrerer Nasennebenhöhlen nennt man auch Pansinusoperation oder bei rein endoskopischer Operation FESS (functionel endoscopic sinus surgery = funktionelle endoskopische Nebenhöhlen Chirurgie). Wir wenden meistens eine Mischung aus endoskopischer und mikroskopischer Nebenhöhlenoperation an.</p>
                        <p>Bei den Kieferhöhlen ist die Erweiterung des belüftenden Nasennebenhöhlen-Kanals am einfachsten, Siebbein und Keilbeinhöhlen sind schwieriger zu erreichen, am Schwierigsten die Stirnhöhlen, deren langer Gang ein komplettes Ausräumen nahezu unmöglich macht.</p>
                        <p>Auf dem obigen Bild sind die kleinen Zellen zwischen den Augenhöhlen die Siebbeinzellen. Sind diese von Polypen ausgefüllte, so leidet der Patient an Kopfschmerzen, Schleimfluß hinten in den Rachen, Kopf- und Augendruck, immer wieder auftretende Entzündungen der Nasennebenhöhlen,  Nasenatmungsbehinderung. Oft sind diese kombiniert mit Polypen in den Stirn- oder Kieferhöhle.</p>
